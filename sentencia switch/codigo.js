let expr = "Banana";

switch(expr){
    case "Banana":
        console.log("Esta fruta es de color amarillo");
        alert("Es Banana");
        break;
    case "Pera":
        console.log("Esta fruta es de color verde claro");
        alert("Es Pera");
        break;
    case "Manzana":
        console.log("Esta fruta es de color rojo");
        alert("Es Manzana")
        break;
    default:
        alert("No es ninguna");
}